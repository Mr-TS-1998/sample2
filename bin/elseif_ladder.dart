void main()
{
  var size='l';

  if(size=='s')
    {
      print("Size is small");
    }
  else if(size=='m')
    {
      print("Size is medium");
    }
  else if(size=='l')
    {
      print("Size is large");
    }
  else if(size=='xl')
    {
      print("Size is extra large");
    }
  else if(size=='xxl')
  {
    print("Size is super extra large");
  }
  else
    {
      print("Invalid Size");
    }
}