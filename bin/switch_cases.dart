void main()
{
  var size='l';

  switch(size)
  {
    case's':print("Size is small");
    break;

    case'm':print("Size is medium");
    break;

    case'l':print("Size is large");
    break;

    case'xl':print("Size is extra large");
    break;

    case'xxl':print("Size is super extra large");
    break;

  }

  print("***************");

  var day=5;

  switch(day)
  {
    case 1:print("SUNDAY");
    break;

    case 2:print("MONDAY");
    break;

    case 3:print("TUESDAY");
    break;

    case 4:print("WEDNESDAY");
    break;

    case 5:print("THURSDAY");
    break;

    case 6:print("FRIDAY");
    break;

    case 7:print("SATURDAY");
    break;

  }

}