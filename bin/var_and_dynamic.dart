void main()
{
  // VAR datatype
  // 'var' is used for assign a variable datatype into a fixed datatype
  // if the variable is assigned as integer type, only integer type datas are allowed.
  var age=20;
  age=23;
  print(age);

  //DYNAMIC datatype
  //'dynamic' is used for assigning a variable with different datatypes.
  dynamic num=21;
  num="NOT AVAILABLE";
  print(num);
}